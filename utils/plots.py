import matplotlib.pyplot as plt
import seaborn as sns
import pandas as pd
import streamlit as st

def plot_prediction(data, pred):
    st.subheader("ğŸ“ˆ ëª¨ë¸ ì˜ˆì¸¡ vs. ì‹¤ì¸¡ ë†ë„")
    fig, ax = plt.subplots(figsize=(6, 4))
